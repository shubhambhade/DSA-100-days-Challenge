#include<iostream>
using namespace std;

int main(){
    // int data type
    int a = 10;
    cout<<"a : "<<a<<endl;

    //char data type
    char c = 'q';
    cout<<"q : " <<c<<endl;

    // float data type
    float  f = 34.45;
    cout<<"f : "<<f<<endl;

    // double data type
    double d = 3663.47784898;
    cout<<"d : "<<d<<endl;

    // bool data type
    bool b = true;
    cout<<"b : "<<b<<endl;
return 0;
}